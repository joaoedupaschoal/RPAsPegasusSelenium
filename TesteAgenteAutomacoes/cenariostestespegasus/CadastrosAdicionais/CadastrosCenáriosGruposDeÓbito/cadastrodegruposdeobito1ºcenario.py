def gerar_datas_validas():
    """Gera datas coerentes para admiss√£o, in√≠cio e fim da escala, e vencimento da CNH."""
    hoje = datetime.today().date()
    
    # Data de admiss√£o entre 10 anos atr√°s e hoje
